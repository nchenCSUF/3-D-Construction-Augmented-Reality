package com.example.augmentedreality3d;

public class User {

    public String fullName, userName, emailId, gender;

    public User(){

    }
    public User(String fullName, String userName, String emailId, String gender) {
        this.fullName = fullName;
        this.userName = userName;
        this.emailId = emailId;
        this.gender = gender;
    }
}
